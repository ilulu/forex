	-- ISABLE_
	ISABLE_ INT(10) ,
	-- BALANCE_SIZE_
	BALANCE_SIZE_ VARCHAR(100) ,
	-- OUT_LINK_URL_
	OUT_LINK_URL_ VARCHAR(200) ,
